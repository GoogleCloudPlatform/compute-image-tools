set -xeu

tee -a /etc/zypp/repos.d/google-cloud-sdk.repo << EOM
[google-cloud-sdk]
name=Google Cloud SDK
baseurl=https://packages.cloud.google.com/yum/repos/cloud-sdk-el7-x86_64
enabled=1
gpgcheck=1
repo_gpgcheck=1
gpgkey=https://packages.cloud.google.com/yum/doc/yum-key.gpg
       https://packages.cloud.google.com/yum/doc/rpm-package-key.gpg
EOM

zypper -n --no-gpg-checks ref
zypper install -y kernel-source git google-cloud-cli
rm -rf /files/distro_kernel && mkdir -p /files/distro_kernel
cp -R /usr/src/linux/* /files/distro_kernel

rpm -q --changelog kernel-default > /files/kernel.changelog

python3.6 kcq.py
